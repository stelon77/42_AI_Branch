from matrix import *

u = Matrix([
    [1., 0.],
    [0., 1.]])
print(u.transpose())
# [1.0, 0.0]
# [0.0, 1.0]
u = Matrix([
    [2., -5., 0.],
    [4., 3., 7.],
    [-2., 3., 4.]])
print(u.transpose())
# [2.0, 4.0, -2.0]
# [-5.0, 3.0, 3.0]
# [0.0, 7.0, 4.0]
u = Matrix([
    [-2., -8., 4.],
    [1., -23., 4.],
    [0., 6., 4.]])
print(u.transpose())
# [-2.0, 1.0, 0.0]
# [-8.0, -23.0, 6.0]
# [4.0, 4.0, 4.0]
